class TestCanvas extends Canvas
{

 //==============================================================================
 //paint()メソッド
 //==============================================================================
 public void paint(Graphics g)
 {
  //ここに描画処理を書きます。
  //ここに描画処理を書きます。
  
  setBackground (Color.red);
  g.setColor(Color.black);
  g.drawRect (20, 20, 100, 200);
  
  //2つの処理を同時におこなうと、連続して処理をしてしまうっぽい
  
 }

}